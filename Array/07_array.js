// Array Destructuring
const arr = ["val1","val2"];
//  If we wanted to perform the below task in a shortcut way then we use array destrucuturing
let var1 = arr[0];
let var2 = arr[1];
console.log("var1: " , var1);
console.log("var2: " , var2);


// Shortcut method -> array destrucuturing
let [v1,v2] = arr;
console.log(v1);
console.log(v2);